.MODEL SMALL
.STACK 100H
.DATA
    PROMPT1 DB 'ENTER A CHARACTER: $'
    PROMPT2 DB 'THE GIVEN CHARACTER IS: $'
.CODE
    MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    
    MOV AH, 9
    LEA DX, PROMPT1
    INT 21H
   
    MOV AH, 1
    INT 21H
    
    CALL NEXT_LINE ;CALLING NEXT_LINE FUNCTION
   
    MOV AH, 9
    LEA DX, PROMPT2
    INT 21H
   
    MOV AH, 2
    MOV DL, AL
    INT 21H
   
    MOV AH, 4CH
    INT 21H
    
    MAIN ENDP

 NEXT_LINE PROC
PUSH AX
PUSH DX
;TYPE YOUR CODE HERE
MOV AH, 2
MOV DL, 0AH
INT 21H

MOV AH, 2
MOV DL, 0DH
INT 21H
;CODE END HERE
POP DX
POP AX
RET
NEXT_LINE ENDP
END MAIN